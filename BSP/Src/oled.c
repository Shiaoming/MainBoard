//函数名：OLED_P6x8Str(unsigned char x,unsigned char y,unsigned char *p)
//功能描述：写入一组标准ASCII字符串
//参数：显示的位置（x,y），y为页范围0～7，要显示的字符串
//返回：无
//==============================================================
void OLED_P6x8Str(unsigned char x,unsigned char y,unsigned char ch[])
{
#ifdef USEOLED
	unsigned char c=0,i=0,j=0;
	while (ch[j]!='\0')
	{
		c =ch[j]-32;
		if(x>126){x=0;y++;}
			OLED_Set_Pos(x,y);
		for(i=0;i<6;i++)
			OLED_WrDat(f6x8[c][i]);
		x+=6;
		j++;
	}
#endif
}

