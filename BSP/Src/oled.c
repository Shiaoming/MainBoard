void OLED_DLY_INT(int time)
{
	OS_ERR err;
	OSTimeDly(1,OS_OPT_TIME_MATCH,&err);
	
	time = time;
}

void OLED_WrDat(unsigned char data)
{
	unsigned char i=8;
	//OLED_CS=0;;
	OLED_DC_Set();
	OLED_DLY_INT(1);
	OLED_SCL_Clr();
	OLED_DLY_INT(1);
	//asm("nop");
	while(i--)
	{
		if(data&0x80){OLED_SDA_Set();
		OLED_DLY_INT(1);}
		else{OLED_SDA_Clr();
		OLED_DLY_INT(1);}
		OLED_SCL_Set();
		OLED_DLY_INT(1);
		asm("nop");;
			//asm("nop");
		OLED_SCL_Clr();
		OLED_DLY_INT(1);
		data<<=1;
	}
	//OLED_CS=1;
}
void OLED_WrCmd(unsigned char cmd)
{
	unsigned char i=8;

	//OLED_CS=0;;
	OLED_DC_Clr();
	OLED_DLY_INT(1);
	OLED_SCL_Clr();
	OLED_DLY_INT(1);
	//asm("nop");
	while(i--)
	{
		if(cmd&0x80)
		{
			OLED_SDA_Set();
			OLED_DLY_INT(1);
		}
		else
		{
			OLED_SDA_Clr();
			OLED_DLY_INT(1);
		}
		OLED_SCL_Set();
		OLED_DLY_INT(1);
		asm("nop");;
		//asm("nop");
		OLED_SCL_Clr();
		OLED_DLY_INT(1);
		cmd<<=1;;
	}
	//OLED_CS=1;
}
void OLED_Set_Pos(unsigned char x, unsigned char y)
{
	OLED_WrCmd(0xb0+y);
	OLED_WrCmd(((x&0xf0)>>4)|0x10);
	OLED_WrCmd((x&0x0f)|0x01);
}

void OLED_Fill(unsigned char bmp_data)
{
	unsigned char y,x;

	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<128;x++)
			OLED_WrDat(bmp_data);
	}
}



void OLED_CLS(void)
{
	unsigned char y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<128;x++)
			OLED_WrDat(0);
	}
}

